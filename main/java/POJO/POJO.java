package POJO;

public class POJO {
    public String Bandname;
    public String hometown;
    public String genre;
    public String last_ablbum;
    public String Singer;

    public POJO(String Bandname, String hometown, String genre, String last_ablbum, String Singer){
        this.Bandname = Bandname;
        this.hometown = hometown;
        this.genre = genre;
        this.last_ablbum = last_ablbum;
        this.Singer = Singer;
    }
}